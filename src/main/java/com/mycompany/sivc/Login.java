    public void validarUsuario(){
        conexion con = new conexion();
        Connection c =con.conectar();
        
        int res=0;
        String user=username.getText();
        String pass = String.valueOf(password.getPassword());
        String SQL = "select * from usuarios where usuario='"+user+"' and contrase침a='"+pass+"'";
        
        try{
            Statement st=c.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
            if(rs.next()){
                res=1;
                MenuGeneral abrir = new MenuGeneral();
                abrir.setVisible(true);
                this.setVisible(false);
            }
            else{
                
                JOptionPane.showMessageDialog(null, "Error de acceso, usuario no registrado o contrase침a incorrecta.");//mensaje temporal o permanente.
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error "+e.getMessage());
        }
    }
           
    
